           <section id="mototaxistas">
  <h2>Mototaxistas</h2>

  <h3>¿Cómo convertirse en mototaxista?</h3>
  <p>1. Inicia sesión en la aplicación.</p>
  <p>2. En el menú principal, selecciona <strong>"Quiero ser mototaxista"</strong>.</p>
  <p>3. Aparecerá un mensaje informándote que debes subir los documentos de tu moto (licencia, SOAT, tarjeta de propiedad, etc).</p>
  <p>4. Si presionas <strong>Aceptar</strong>, se abrirá el formulario para subir tus documentos.</p>
  <p>5. Si presionas <strong>Cancelar</strong>, el proceso se detendrá y no podrás convertirte en mototaxista.</p>
  <p>6. Una vez tus documentos sean aprobados, podrás acceder a la sección para recibir y aceptar servicios.</p>

  <h3>¿Cómo conectarse para recibir servicios?</h3>
  <p>1. En la sección de mototaxistas, verás un botón con un ícono de encendido/apagado.</p>
  <p>2. Presiona ese botón para cambiar tu estado a <strong>Conectado</strong>. Esto activará la recepción de solicitudes de servicio.</p>
  <p>3. Al conectarte, se reproducirá un sonido como confirmación.</p>
  <p>4. Si no deseas recibir servicios automáticamente, puedes mantenerte desconectado y aceptar servicios de forma manual desde la lista de solicitudes.</p>
  <p>5. Puedes presionar el botón nuevamente en cualquier momento para desconectarte y dejar de recibir solicitudes.</p>

  <h3>¿Cómo aceptar un servicio?</h3>
  <p>1. Si estás conectado, los servicios se te asignan automáticamente según disponibilidad y cercanía.</p>
  <p>2. Si estás desconectado, puedes revisar manualmente las solicitudes disponibles en la lista.</p>
  <p>3. Cada solicitud muestra información como el punto de origen, destino y valor.</p>
  <p>4. Para aceptar un servicio manualmente, haz clic en el botón <strong>Aceptar</strong> junto a la solicitud.</p>
  <p>5. Una vez aceptado el servicio, recibirás los detalles completos para realizar el viaje.</p>

  <h3>¿Qué sucede al finalizar un servicio?</h3>
  <p>1. Cuando terminas un viaje, debes presionar el botón <strong>"Terminar servicio"</strong>.</p>
  <p>2. Se abrirá un formulario donde debes indicar si el cliente pagó el servicio.</p>
  <p>3. También puede terminar el servicio si el cliente estuvo ausente.</p>
  <p>4. Finalmente, puedes calificar al cliente y dejar tu opinión.</p>
  <p>5. Estos pasos son necesarios para cerrar correctamente el servicio y quedar disponible para nuevos viajes.</p>
</section>



